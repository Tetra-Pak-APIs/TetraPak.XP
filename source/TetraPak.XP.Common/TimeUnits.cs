namespace TetraPak.XP
{
    /// <summary>
    ///   Textual qualifiers to represent time-based unit of measurements. 
    /// </summary>
    public static class TimeUnits
    {
        /// <summary>
        ///   Qualifies day/days.
        /// </summary>
        public const string Days = "d";

        /// <summary>
        ///   Qualifies hour/hours.
        /// </summary>
        public const string Hours = "h";

        /// <summary>
        ///   Qualifies minute/minutes.
        /// </summary>
        public const string Minutes = "m";

        /// <summary>
        ///   Qualifies second/seconds.
        /// </summary>
        public const string Seconds = "s";

        /// <summary>
        ///   Qualifies millisecond/milliseconds.
        /// </summary>
        public const string Milliseconds = "ms";
    }
}